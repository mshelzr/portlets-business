package com.ext.portlet.halcon.dto;

public class CampaniaRequestBean {

	private String tipoDocumento;
	private String numeroDocumento;
	private String codigoCanal;
	private String codigoGrupoProducto;
	private String getRefConsultarCampanias;
	private String codigoProducto;

	
	
	public String getTipoDocumento() {
		return tipoDocumento;
	}
	public void setTipoDocumento(String tipoDocumento) {
		this.tipoDocumento = tipoDocumento;
	}
	public String getNumeroDocumento() {
		return numeroDocumento;
	}
	public void setNumeroDocumento(String numeroDocumento) {
		this.numeroDocumento = numeroDocumento;
	}
	public String getCodigoCanal() {
		return codigoCanal;
	}
	public void setCodigoCanal(String codigoCanal) {
		this.codigoCanal = codigoCanal;
	}
	public String getCodigoGrupoProducto() {
		return codigoGrupoProducto;
	}
	public void setCodigoGrupoProducto(String codigoGrupoProducto) {
		this.codigoGrupoProducto = codigoGrupoProducto;
	}
	public String getGetRefConsultarCampanias() {
		return getRefConsultarCampanias;
	}
	public void setGetRefConsultarCampanias(String getRefConsultarCampanias) {
		this.getRefConsultarCampanias = getRefConsultarCampanias;
	}
	public String getCodigoProducto() {
		return codigoProducto;
	}
	public void setCodigoProducto(String codigoProducto) {
		this.codigoProducto = codigoProducto;
	}

	
	
	
}
